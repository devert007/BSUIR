#include <iostream>
#include <math.h>
#include <iomanip>
using namespace std;
int main()
{
  double a, b, n, h;
  cout << "??????? ? ?????? ????? ????????: ";
  cin >> a;
  cout << "??????? ????? ?????? ???????????: ";
  cin >> b;
  cout << "??????? ???: ";
  cin >> h;
  cout << "??????? ?????????? n ? ????????: ";
  cin >> n;
  double x = a;
  while (x <= b + h / 2)
  {
    double s = 1, m = 1;
    int k = 1;
    for (int i = 1; i <= n; i++)
    {
      k *= 2 * i + 1;
      m *= pow(x, (2 * i + 1)) / k;
      s += m;
    }

    cout << setw(15) << x << setw(15) << s << '\n';
    x += h;
  }

  return 0;
}